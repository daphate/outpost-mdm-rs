_REGEX_META = (".", "*", "?", "+", "[", "(", "\\", "|", "^", "$")

def detect_query_domain(query: str) -> str | None:
    ql = query.lower()
    counts = {dom: 0 for dom in DOMAIN_TRIGGERS}
    for dom, kws in DOMAIN_TRIGGERS.items():
        for kw in kws:
            try:
                if any(c in kw for c in _REGEX_META):
                    if re.search(kw, ql):
                        counts[dom] += 1
                else:
                    if kw in ql:
                        counts[dom] += 1
            except re.error:
                if kw in ql:
                    counts[dom] += 1
    top_dom = max(counts, key=counts.get)
    if counts[top_dom] == 0:
        return None
    return top_dom


def classify_chunk(text: str) -> str | None:
    """Грубая классификация чанка по доменам — по триггерам в первых 1500 chars."""
    tl = text[:1500].lower()
    counts = {dom: 0 for dom in DOMAIN_TRIGGERS}
    for dom, kws in DOMAIN_TRIGGERS.items():
        for kw in kws:
            try:
                if any(c in kw for c in _REGEX_META):
                    if re.search(kw, tl):
                        counts[dom] += 1
                else:
                    if kw in tl:
                        counts[dom] += 1
            except re.error:
                if kw in tl:
                    counts[dom] += 1
    top = max(counts, key=counts.get)
    return top if counts[top] >= 2 else None


