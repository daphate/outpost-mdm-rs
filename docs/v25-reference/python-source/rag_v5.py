def find_safety_pins(query: str, chunks: list[dict], sims: np.ndarray) -> list[dict]:
    """Найти чанки которые ОБЯЗАНЫ быть в контексте для life-critical запроса.

    Для каждого активного pin: ищем в указанных файлах чанк, содержащий хотя бы
    один must_contain substring, выбираем по максимальной similarity к запросу.
    Возвращаем уникальные чанки (по id), помеченные as from_pin=True.
    """
    ql = query.lower()
    pinned = []
    seen_ids = set()
    for pin in SAFETY_PINS:
        if not re.search(pin["trigger"], ql, re.IGNORECASE):
            continue
        if pin.get("trigger_not") and re.search(pin["trigger_not"], ql, re.IGNORECASE):
            continue
        # Кандидаты: чанки из указанных файлов, содержащие must_contain
        best_idx = -1
        best_sim = -1.0
        files_lc = [f.lower() for f in pin["files"]]
        must = pin["must_contain"]
        for i, c in enumerate(chunks):
            if c["id"] in seen_ids:
                continue
            f_lc = (c.get("file") or "").lower()
            if not any(fk in f_lc for fk in files_lc):
                continue
            text = c["text"]
            if not any(m.lower() in text.lower() for m in must):
                continue
            if sims[i] > best_sim:
                best_sim = float(sims[i])
                best_idx = i
        if best_idx >= 0:
            c = chunks[best_idx]
            pinned.append({
                **c, "raw_sim": float(sims[best_idx]),
                "from_pin": True, "pin_name": pin["name"],
                # Высокий score чтобы pin не вытеснился diversity-фильтром
                "score": 100.0 + float(sims[best_idx]),
            })
            seen_ids.add(c["id"])
    return pinned


# === LOAD ====================================================================

def load_db(db_path: str | Path) -> tuple[list[dict], np.ndarray, sqlite3.Connection]:
    db_path = str(db_path)
    conn = sqlite3.connect(db_path)
    rows = conn.execute(
        "SELECT e.id, e.vector, c.text, c.source, c.doc_type, c.section, c.file "
        "FROM embeddings e JOIN chunks c ON c.id=e.id"
    ).fetchall()
    chunks = []
    for rid, blob, text, src, dt, sec, fl in rows:
        v = np.array(struct.unpack(f"<{len(blob)//4}f", blob), dtype=np.float32)
        chunks.append({
            "id": rid, "vec": v, "text": text,
            "source": src or "?", "doc_type": dt or "guide",
            "section": sec or "", "file": fl or "",
            "_domain": classify_chunk(text),
        })
    vec_stack = np.stack([c["vec"] for c in chunks])
    return chunks, vec_stack, conn


