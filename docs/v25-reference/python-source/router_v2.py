_REGEX_META = set(".[](){}^$|+?*\\")

@dataclass
class RouteResult:
    domain: str | None
    confidence: float            # 0..1, нормализовано относительно top-domain'а
    second: str | None
    second_score: float
    counts: dict[str, float]


def route(query: str) -> RouteResult:
    ql = query.lower()
    counts: dict[str, float] = {dom: 0.0 for dom in DOMAIN_TRIGGERS_V2}
    for dom, kws in DOMAIN_TRIGGERS_V2.items():
        for kw, w in kws:
            if any(c in kw for c in _REGEX_META):
                try:
                    if re.search(kw, ql):
                        counts[dom] += w
                except re.error:
                    if kw in ql:
                        counts[dom] += w
            else:
                if kw in ql:
                    counts[dom] += w
    sorted_doms = sorted(counts.items(), key=lambda x: x[1], reverse=True)
    top_dom, top_score = sorted_doms[0]
    second_dom, second_score = sorted_doms[1] if len(sorted_doms) > 1 else (None, 0.0)
    if top_score == 0:
        return RouteResult(None, 0.0, None, 0.0, counts)
    # confidence = доля top'а в сумме top + second
    denom = top_score + second_score if second_score > 0 else top_score
    confidence = top_score / denom if denom else 0.0
    return RouteResult(top_dom, confidence, second_dom, second_score, counts)


def detect_query_domain(query: str, min_score: float = 0.5) -> str | None:
    """Drop-in замена для rag_v6.detect_query_domain.
    Возвращает домен только если score не нулевой; иначе None.
    """
    r = route(query)
    if r.domain is None:
        return None
    return r.domain


