-- TOTP replay-защита (RFC 6238 §5.2 «one-time only use»).
--
-- Храним последний успешно использованный 30-секундный timestep на пользователя.
-- Логин-2FA принимает код только если его timestep СТРОГО больше сохранённого,
-- после чего сохраняет новый timestep. Это исключает повторное использование
-- одного и того же 6-значного кода в течение окна его валидности (текущий ±1
-- шаг) — например, перехваченного по плечу или в прокси.
--
-- NULL = пользователь ещё ни разу не проходил 2FA после появления колонки;
-- первый валидный код принимается и фиксирует timestep.
ALTER TABLE users ADD COLUMN totp_last_step INTEGER;
