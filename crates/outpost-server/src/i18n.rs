//! Strongly-typed translations for the admin UI.
//!
//! Why not `fluent-templates` / `gettext` / `i18n-embed`?
//! - Compile-time check: missing key on a template is a build error, not
//!   a runtime placeholder. Adding a new language is one big literal map.
//! - Zero runtime cost: `&'static Strings` per request.
//! - Plays nicely with Askama 0.13: each template just takes `s: &'static Strings`
//!   and references `{{ s.field }}`.
//!
//! Conventions:
//! - **Russian is the default.** A fresh visitor with no cookie gets RU.
//! - **Technical identifiers stay English in both locales** — `API`, `OTLP`,
//!   `TOTP`, `SHA-256`, `JSON`, `JWT`, `mDM`, `Bearer`, etc. Don't translate
//!   ENVIRONMENT VARIABLE names or HTTP method words.
//! - Operator-facing buttons / labels / hints are translated.
//! - Numbers, dates, durations stay locale-neutral for now (no plural rules).
//!
//! Adding a new locale: implement another `pub static STRINGS_<XX>` filling
//! every field, add it to `parse_locale` and `Locale::all`.

use axum::http::request::Parts;
use axum::http::header;
use std::sync::OnceLock;

/// Locales we ship. Names are ISO 639-1.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Locale {
    Ru,
    En,
}

impl Locale {
    /// Default for a request with no `outpost_lang` cookie.
    pub const DEFAULT: Locale = Locale::Ru;

    pub fn code(self) -> &'static str {
        match self {
            Locale::Ru => "ru",
            Locale::En => "en",
        }
    }

    pub fn label(self) -> &'static str {
        match self {
            Locale::Ru => "Русский",
            Locale::En => "English",
        }
    }

    pub fn all() -> &'static [Locale] {
        &[Locale::Ru, Locale::En]
    }

    pub fn strings(self) -> &'static Strings {
        match self {
            Locale::Ru => strings_ru(),
            Locale::En => strings_en(),
        }
    }
}

pub fn parse_locale(s: &str) -> Option<Locale> {
    match s.trim().to_ascii_lowercase().as_str() {
        "ru" | "ru-ru" | "ru_ru" => Some(Locale::Ru),
        "en" | "en-us" | "en-gb" | "en_us" => Some(Locale::En),
        _ => None,
    }
}

/// Read `outpost_lang` cookie; fall back to `Accept-Language` header;
/// fall back to `Locale::DEFAULT` (Russian).
pub fn from_request(parts: &Parts) -> Locale {
    // 1) explicit cookie
    if let Some(hdr) = parts.headers.get(header::COOKIE).and_then(|v| v.to_str().ok()) {
        for kv in hdr.split(';') {
            if let Some(v) = kv.trim().strip_prefix("outpost_lang=")
                && let Some(loc) = parse_locale(v)
            {
                return loc;
            }
        }
    }
    // 2) Accept-Language
    if let Some(hdr) = parts
        .headers
        .get(header::ACCEPT_LANGUAGE)
        .and_then(|v| v.to_str().ok())
    {
        // Crude parse — take the first token before ';' or ',', try direct
        // match, then strip the q-factor noise.
        for tag in hdr.split(',').map(|s| s.trim().split(';').next().unwrap_or("")) {
            if let Some(loc) = parse_locale(tag) {
                return loc;
            }
        }
    }
    Locale::DEFAULT
}

